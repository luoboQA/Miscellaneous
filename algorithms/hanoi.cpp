/* 递归思考
为了把最大的盘子从 A 安全地搬到 C，我们必须经历以下三个必然的步骤
步骤 1：清空 A 柱（把 n-1 个小盘子全部移走）
步骤 2：把最大的盘子从 A 移到 C
步骤 3：把 n-1 个小盘子从 B 移到 C（把它们放在最大的盘子上面）
因此我们可以得出一个递归公式：
   移动 n 个盘子 = 
   移动 n-1 个盘子 (借助目标柱)
   + 移动最大盘子
   + 移动 n-1 个盘子 (从辅助柱移到目标柱)
===========================================================================
第一步：改变提问方式
线性思维：hanoi(3) 怎么移动？我要一步一步模拟出来…
递归思维：如果我已经有了一个能移动 2 个盘子的方法，我能不能用它来解决 3 个盘子的？

第二步：用黑盒思维理解
把hanoi 函数看作一个神奇的黑盒：

功能：这个盒子能把 n 个盘子，从 A 柱搬到 C 柱（借用 B 柱），并遵守所有规则。
前提：我们暂时不管盒子内部是怎么工作的。
按照我们之前的逻辑，移动 n 个盘子需要三步：

把 n-1 个盘子从 A 移到 B。（使用黑盒）
把最大盘子从 A 移到 C。（直接操作）
把 n-1 个盘子从 B 移到 C。（再次使用黑盒）

第三步：层层“套娃”直到问题变简单（基准情形）
为了移动 3 个盘子，我需要一个能移动 2 个盘子的黑盒。
为了移动 2 个盘子，我需要一个能移动 1 个盘子的黑盒。
为了移动 1 个盘子……嗯？移动 1 个盘子还需要黑盒吗？

不需要！当 n == 1 时，问题已经简单到不需要再分解了，直接移动就行
这就是递归的基准情形
if (n == 1) {
    move(A, C);  // 直接移动，不再分解
}
归纳证明正确性：
数学归纳法	                                         汉诺塔递归函数 hanoi(n)
基准：n=1 时，移动 1 个盘子直接成立。	           if (n == 1) move(A, C); （直接移动，绝对正确）
归纳假设：假设 hanoi(n-1) 能正确移动 n-1 个盘子。	   在 hanoi(n) 中，直接调用 hanoi(n-1, ...)，并相信它能完成工作。
归纳步骤：利用假设，证明 hanoi(n) 成立。	      利用 hanoi(n-1) 把上面的盘子移开，移动最大的，再用 hanoi(n-1) 移回来。
*/
#include <iostream>
using namespace std;
void move(char A, char B);
void hanoi(int n, char A, char B, char C);
int main(int argc, char const *argv[]) {
    cout << "请输入盘子数:";
    int disks;
    cin >> disks;
    hanoi(disks, 'A', 'B' , 'C');

    return 0;
}

void move(char A, char B) { cout << A << "->" << B << endl;}

void hanoi(int n, char A, char B, char C) {
    if (n == 1) {
        move(A, C);
    } else {
        hanoi(n - 1, A, C, B);
        move(A, C);
        hanoi(n - 1, B, A, C);
    }
}
